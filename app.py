def build_lstm():
    return Sequential([
        Embedding(vocab_size, embedding_dim, input_length=40),
        LSTM(64, kernel_initializer=initializer, recurrent_initializer=initializer),
        Dense(num_classes, activation='softmax', kernel_initializer=initializer)
    ])


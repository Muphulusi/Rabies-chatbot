def store_new_qa(sepedi, english, answer):
    new_data = {
        "sepedi": sepedi,
        "english": english,
        "answer": answer
    }

    with open("qa_memory.json", "a", encoding="utf-8") as f:
        f.write(json.dumps(new_data) + "\n")

SIM_THRESHOLD = 0.75

def chatbot(sepedi_question):

    # 1. Translate ALWAYS
    english_q = translate_to_english(sepedi_question)

    if not english_q or english_q.strip() == "":
        english_q = sepedi_question

    # 2. Retrieve best match
    idx, score = retrieve_answer(english_q)

    # 3. High confidence → dataset answer
    if score >= SIM_THRESHOLD:
        return {
            "answer": kb_answers[idx],
            "source": "dataset",
            "score": score,
            "translated": english_q
        }

    # -----------------------------
    # 4. HYBRID FALLBACK (RAG)
    # -----------------------------
    top_k = retrieve_top_k(english_q, k=3)

    context = ""
    for q, a, s in top_k:
        context += f"Q: {q}\nA: {a}\n\n"

    prompt = f"""
Use the context below to answer the question.
If unsure, say you are not certain.

Context:
{context}

Question:
{english_q}
"""

    response = generator(prompt, max_length=200)[0]["generated_text"]

    answer = response

    # 5. Learn new QA
    if len(answer) > 10:
        store_new_qa(sepedi_question, english_q, answer)

    return {
        "answer": answer,
        "source": "hybrid",
        "score": score,
        "translated": english_q
    }


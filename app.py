def custom_train_test_split(X, y, test_size_per_class=2, random_state=42):
    np.random.seed(random_state)

    train_idx = []
    test_idx = []

    classes = np.unique(y)

    for cls in classes:
        cls_idx = np.where(y == cls)[0]
        np.random.shuffle(cls_idx)

        test_samples = cls_idx[:test_size_per_class]
        train_samples = cls_idx[test_size_per_class:]

        test_idx.extend(test_samples)
        train_idx.extend(train_samples)

    return (
        X[train_idx], X[test_idx],
        y[train_idx], y[test_idx]
    )

